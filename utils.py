                if (gt_class == pred_class):
                    gt.append(gt_class)
                    pred.append(pred_class)

                    gt_area.append(gt_mask_area[i])
                    pred_area.append(pred_mask_area[j])

                    gt_SN.append(gt_bboxes[i][2] - gt_bboxes[i][0])
                    gt_EW.append(gt_bboxes[i][3] - gt_bboxes[i][1])
                    pred_SN.append(pred_bboxes[j][2] - pred_bboxes[j][0])
                    pred_EW.append(pred_bboxes[j][3] - pred_bboxes[j][1])

                    gt_x, gt_y = cv2_center_point(gt_mask[:, :, i])
                    pred_x, pred_y = cv2_center_point(pred_mask[:, :, j])
                    gt_center_x.append(gt_x)
                    gt_center_y.append(gt_y)
                    pred_center_x.append(pred_x)
