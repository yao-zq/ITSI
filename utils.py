def cv2_center_point(mask_i):
    """
    mask_i : mask[:,:,i]
    :return: x,y
    """
    contours, hierarchy = cv2.findContours(mask_i, cv2.RETR_EXTERNAL, cv2.CHAIN_APPROX_NONE)

    if len(contours) == 1:
        x = cv2.moments(contours[0])['m10'] / (cv2.moments(contours[0])['m00']+0.0001)
        y = cv2.moments(contours[0])['m01'] / (cv2.moments(contours[0])['m00']+0.0001)
    elif len(contours) > 1:
        area = []
        for i in range(len(contours)):
            area.append(cv2.contourArea(contours[i]))
        index_max = np.argmax(area)
        x = cv2.moments(contours[index_max])['m10'] / (cv2.moments(contours[index_max])['m00']+0.0001)
        y = cv2.moments(contours[index_max])['m01'] / (cv2.moments(contours[index_max])['m00']+0.0001)
    else:
        raise 'num of contours error'
    return x, y


def gt_pred_paras(gt_class_ids, gt_bboxes, gt_mask, gt_mask_area,
                 pred_class_ids, pred_bboxes, pred_mask, pred_mask_area,
                 iou_tresh=0.5):
    """
        Given a list of ground truth and predicted classes and their boxes,
        this function associates the predicted classes to their gt classes using a given Iou (Iou>= 0.5 for example) and returns
        two normalized lists of len = N containing the gt and predicted classes,
        filling the non-predicted and miss-predicted classes by the background class (index 0).
        Args    :
            gt_class_ids   :    list of gt classes of size N1
            pred_class_ids :    list of predicted classes of size N2
            gt_bboxes      :    list of gt boxes [N1, (x1, y1, x2, y2)]
            pred_bboxes    :    list of pred boxes [N2, (x1, y1, x2, y2)]

        Returns :
            gt             :    list of size N
            pred           :    list of size N
    """

    # dict containing the state of each gt and predicted class (0 : not associated to any other class, 1 : associated
    # to a class)
